from typing import List


class Solution:
    def findLength(self, nums1: List[int], nums2: List[int]) -> int:
        res = 0
        for i in range(len(nums1)):
            for j in range(len(nums2)):
                if nums1[i] == nums2[j]:
                    i1, j1, l = i, j, 0
                    while i1 < len(nums1) and j1 < len(nums2) and nums1[i1] == nums2[j1]:
                        l += 1
                        i1 += 1
                        j1 += 1

                    res = max(res, l)
        return res


